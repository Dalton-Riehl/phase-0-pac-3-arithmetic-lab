function add(num1, num2) {
    return num1 + num2;
}
add(9, 10);

function subtract(num1, num2) {
    return num1 - num2;
}
subtract(18, 7);

function multiply(num1, num2) {
    return num1 * num2;
}
multiply(23, 3);

function divide(num1, num2) {
    return num1 / num2;
}
divide(60, 4);

function increment(n) {
    n++;
    return n;
}
increment(50);

function decrement(n) {
    n--;
    return n;
}
decrement(420);

function makeInt(n) {
    return parseInt(n, 10);
}
makeInt(335.951);

function preserveDecimal(n) {
    return parseFloat(n);
}
preserveDecimal(98.7654321);
